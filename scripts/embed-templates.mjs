import { readdirSync, readFileSync, writeFileSync } from 'fs'
import { join, dirname } from 'path'
import { fileURLToPath } from 'url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const templatesDir = join(__dirname, '..', 'public', 'templates')
const outputFile = join(__dirname, '..', 'src', 'data', 'templates-embedded.ts')

const files = readdirSync(templatesDir).filter(
  (f) => f.endsWith('.xlsx') || f.endsWith('.docx') || f.endsWith('.pptx')
)

const mimeTypes = {
  xlsx: 'application/vnd.openxmlformats-officedocument.spreadsheetml.sheet',
  docx: 'application/vnd.openxmlformats-officedocument.wordprocessingml.document',
  pptx: 'application/vnd.openxmlformats-officedocument.presentationml.presentation',
}

let output = '// Auto-generated by scripts/embed-templates.mjs — do not edit\n\n'
output += 'export const embeddedTemplates: Record<string, { data: string; mime: string }> = {\n'

for (const file of files) {
  const ext = file.split('.').pop()
  const mime = mimeTypes[ext] || 'application/octet-stream'
  const data = readFileSync(join(templatesDir, file)).toString('base64')
  output += `  '${file}': {\n    data: '${data}',\n    mime: '${mime}',\n  },\n`
}

output += '}\n'

writeFileSync(outputFile, output, 'utf8')
console.log(`Embedded ${files.length} templates into ${outputFile}`)
